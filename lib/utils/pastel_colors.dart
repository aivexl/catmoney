  /// Get default color for account by name
  static Color getDefaultColorForAccount(String name) {
    switch (name.toLowerCase()) {
      case 'cash':
        return palette[3]; // Mint green
      case 'card':
        return palette[4]; // Blue
      default:
        return palette[0]; // Pink
